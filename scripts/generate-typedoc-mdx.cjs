const { execSync } = require("node:child_process");
const { mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } = require("node:fs");
const { join, relative, resolve } = require("node:path");

const root = resolve(__dirname, "..");
const docsRoot = join(root, "docs");
const docsAuto = join(docsRoot, "reference-auto");
const tsconfigPath = join(root, "packages", "core", "tsconfig.json");

const sections = [
  {
    title: "Core exports",
    slug: "core",
    entryPoints: ["packages/core/src/index.ts"],
  },
  {
    title: "McpProxy",
    slug: "mcp-proxy",
    entryPoints: ["packages/core/src/proxy/McpProxy.ts"],
  },
  {
    title: "McpServer",
    slug: "mcp-server",
    entryPoints: ["packages/core/src/server/McpServer.ts"],
  },
  {
    title: "Transports",
    slug: "transports",
    entryPoints: ["packages/core/src/transports/client/StdioTransport.ts"],
  },
  {
    title: "Logger",
    slug: "logger",
    entryPoints: ["packages/core/src/logger.ts"],
  },
  {
    title: "Types",
    slug: "types",
    entryPoints: ["packages/core/src/types.ts"],
  },
  {
    title: "Name mapping",
    slug: "name-mapping",
    entryPoints: ["packages/core/src/nameMapping.ts"],
  },
];

const header = `---\ntitle: "API Reference (Auto)"\ndescription: "Autogenerated API reference from TypeScript sources."\n---\n\n# API Reference (Auto)\n\nThis section is autogenerated from TypeScript source files using TypeDoc. Each area is grouped into collapsible sections.\n\n<Note>\n  Run \`pnpm docs:generate\` after API changes to refresh this section.\n</Note>\n`;

const readMarkdownFiles = (dir) =>
  readdirSync(dir)
    .filter((file) => file.endsWith(".md"))
    .sort()
    .map((file) => ({
      name: file.replace(/\.md$/, ""),
      content: readFileSync(join(dir, file), "utf8").trim(),
    }));

const collectMarkdown = (dir) => {
  if (!statSync(dir).isDirectory()) {
    return [];
  }

  const entries = [];
  const stack = [dir];
  while (stack.length > 0) {
    const current = stack.pop();
    if (!current) {
      continue;
    }
    const items = readdirSync(current);
    for (const item of items) {
      const full = join(current, item);
      if (statSync(full).isDirectory()) {
        stack.push(full);
        continue;
      }
      if (!item.endsWith(".md")) {
        continue;
      }
      const name = relative(dir, full).replace(/\\/g, "/").replace(/\.md$/, "");
      if (name.endsWith("README") || name === "README") {
        continue;
      }
      entries.push({
        name,
        content: readFileSync(full, "utf8").trim(),
      });
    }
  }

  return entries.sort((a, b) => a.name.localeCompare(b.name));
};

const safeTitle = (value) => value.replace(/`/g, "");

const buildAccordion = (title, body) => {
  let cleaned = body.replace(/^#\s+.*$/gm, "");
  cleaned = cleaned.replace(/\[(.*?)\]\([^)]*\)/g, "$1");
  cleaned = cleaned.replace(/^\s*\*\*.*?\*\*\s*$/gm, "");
  cleaned = cleaned.replace(/^\s*\*\*\*\s*$/gm, "");
  cleaned = cleaned.replace(/^\s*##+\s+Pk\s*$/gm, "");
  cleaned = cleaned.replace(/^\s*####\s+Pk\s*$/gm, "");
  cleaned = cleaned.replace(/\n{3,}/g, "\n\n").trim();
  const content = cleaned.length ? cleaned : "No documentation generated.";
  return `  <Accordion title="${safeTitle(title)}">\n\n${content}\n\n  </Accordion>`;
};

const writeOutput = (filePath, content) => {
  mkdirSync(join(filePath, ".."), { recursive: true });
  writeFileSync(filePath, content, "utf8");
};

const runTypedoc = (section) => {
  const outputDir = join(root, "tmp", "typedoc", section.slug);
  const entryPoints = section.entryPoints.map((entry) => join(root, entry));
  const cmd = [
    "pnpm",
    "exec",
    "typedoc",
    "--plugin",
    "typedoc-plugin-markdown",
    "--tsconfig",
    tsconfigPath,
    "--out",
    outputDir,
    "--githubPages",
    "false",
    "--readme",
    "none",
    "--excludeInternal",
    "true",
    "--excludePrivate",
    "true",
    "--excludeProtected",
    "true",
    ...entryPoints,
  ];

  execSync(cmd.join(" "), { stdio: "inherit", cwd: root });
  return outputDir;
};

const generate = () => {
  const accordionGroups = [];

  for (const section of sections) {
    const outputDir = runTypedoc(section);
    const markdownFiles = collectMarkdown(outputDir);
    if (markdownFiles.length === 0) {
      continue;
    }

    const accordions = markdownFiles
      .map((item) => buildAccordion(`${section.title}: ${item.name}`, item.content))
      .join("\n\n");

    accordionGroups.push(`## ${section.title}\n\n<AccordionGroup>\n${accordions}\n</AccordionGroup>`);
  }

  const body = accordionGroups.join("\n\n");
  writeOutput(join(docsAuto, "index.mdx"), `${header}\n\n${body}\n`);

  const statusPath = join(docsAuto, "status.mdx");
  writeOutput(
    statusPath,
    `---\ntitle: "Autogen status"\ndescription: "How the API reference is generated."\n---\n\nThe API reference is generated via TypeDoc and stored in \`docs/reference-auto\`.\n\nRun:\n\n\`pnpm docs:generate\`\n\nThis regenerates the MDX content from TypeScript sources in \`packages/core/src\`.\n`
  );
};

generate();
